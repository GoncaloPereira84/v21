                    <div class="title">DIREITOS DOS TITULARES DOS DADOS PESSOAIS</div>
                    <div class="content">
                        O RGPD confere ao titular dos dados pessoais um conjunto de direitos que têm que ser obrigatoriamente 
                        salvaguardados e que são os seguintes:
                        <br><br>
                        <b>Direito à Informação</b>
                        <br>
                        O RGPD determina que deve ser fornecido aos titulares dos dados pessoais um conjunto de informações. Tem que ser 
                        obrigatoriamente dada a seguinte informação:
                        <br>
                        a) Identidade e contactos da empresa/responsável pelo tratamento dos dados ou do seu representante.
                        <br>
                        b) No caso de a recolha ser direta, tem que se dizer irá existir comunicação de dados a entidades terceiras e 
                        se a mesma constitui ou não uma obrigação legal ou contratual e as consequências do não fornecimento 
                        dos dados.
                        <br>
                        c) Categoriais dos dados pessoais.
                        <br>
                        d) Quais são os fins a que se destinam os dados.
                        <br>
                        e) Prazo de conservação dos dados.
                        <br>
                        f) A existência do direito de acesso, retificação, apagamento e limitação do tratamento.
                        <br>
                        g) A existência do direito de se opor ao tratamento.
                        <br>
                        h) Se o tratamento dos dados se basear no consentimento do seu titular, tem que se informar que este pode 
                        retirar o consentimento a qualquer momento.
                        <br>
                        i) A existência do direito de não sujeição a decisões automatizadas, incluindo a definição de perfis.
                        <br>
                        j) O direito à portabilidade dos dados.
                        <br>
                        k) O direito ao conhecimento da existência de uma violação de dados.
                        <br>
                        l) O direito a reclamar para CNPD.
                        <br>
                        m) No caso de a recolha ser indireta, além disto é necessário informar quais são as categorias de dados que 
                        vão ser recolhidos e as fontes dos dados.
                        <br>
                        <br>
                        <b>Direito de Acesso</b>
                        <br>
                        Os titulares dos dados têm o direito de saber se os seus dados pessoais estão ou não a ser tratados, se foram 
                        transmitidos para outras entidades, qual o destino que lhe foi dados, bem como aceder aos seus dados e a todas as 
                        informações respeitantes às atividades de tratamento. 
                        <br>
                        <br>
                        <b>Direito de Retificação</b>
                        <br>
                        Os titulares dos dados têm direito a obterem a retificação dos mesmos sempre que estejam desatualizados, incorretos 
                        ou incompletos.
                        <br>
                        <br>
                        <b>Direito ao Apagamento</b>
                        <br>
                        Este direito é uma das grandes novidades do RGPD, e também é referido como o “direito a ser esquecido”.
                        O mesmo confere ao titular dos dados pessoais o direito a solicitar às empresas/responsáveis pelo tratamento o 
                        pagamento dos seus dados.
                        <br>
                        Assim, os titulares dos dados pessoais têm o direito a obter a sua eliminação, mas desde que:
                        <br>
                        • Os dados sejam desnecessários para as finalidades para as quais foram recolhidos e tratados;
                        <br>
                        • O titular retire o seu consentimento, quando o tratamento for fundamentado neste e não exista outro 
                        fundamento legal para o tratamento de dados;
                        <br>
                        • O titular se oponha ao tratamento de dados pessoais utilizados para fins automatizados;
                        <br>
                        • Quando os dados pessoais tenham sido tratados de forma ilícita.
                        <br>
                        <br>
                        <b>Direito à Limitação do Tratamento</b>
                        <br>
                        O titular dos dados pessoais tem o direito de exigir a limitação do tratamento dos seus dados.
                        <br>
                        <br>
                        <b>Direito de Portabilidade dos Dados</b>
                        <br>
                        O titular dos dados poder solicitar os seus dados pessoais, num formato de uso comum, ou solicitar a sua transferência 
                        para outra empresa/responsável pelo tratamento dos dados.
                        <br>
                        No entanto, o titular dos dados apenas poderá exigir que os seus dados sejam entregues a outro responsável pelo 
                        tratamento quando tal for tecnicamente possível. 
                        <br>
                        <br>
                        <b>Direito de Oposição e Decisões Individuais Automatizadas</b>
                        <br>
                        O titular dos dados tem o direito de não ser sujeito a este tipo de decisões automatizadas, podendo-se opor, a 
                        qualquer momento, ao tratamento dos dados pessoais que lhe respeitem, incluindo a definição de perfis.
                        <br>
                        <br>
                        <b>Direito a Retirar o seu Consentimento</b>
                        <br>
                        O titular dos dados pode retirar o seu consentimento a qualquer momento.
                        <br>
                        A qualquer momento pode deixar de receber as nossas comunicações, ofertas, novidades e eventos, utilizando o link 
